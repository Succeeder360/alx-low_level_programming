#include "main.h"
/**
 *postitive_or_negative- test for positive or negative number
 *@i: parameter for getting any integer
 */

void postitive_or_negative(int i)
{
if (i > 0)
{
printf("%d is positive\n", i);
}
else if (i < 0)
{
printf("%d is negative\n", i);
}
else
{
printf("%d is zero\n", i);
}
}

